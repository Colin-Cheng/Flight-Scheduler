    private void CancelPassengerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CancelPassengerButtonActionPerformed
        // TODO add your handling code here:
        String cancel_Passenger = CancelPassenger.getItemAt (CancelPassenger.getSelectedIndex());
        Date cancel_Date = Date.valueOf(CancelDate.getSelectedItem().toString());
        int number = 0;
        String status = "";
        String currentFlight;
        currentFlight = Booking.getPassengerBooking(cancel_Passenger, cancel_Date);
        if (currentFlight != null)
        {
            System.out.println("Passenger was found in bookings, removing now");
            System.out.println("Current flight is " + currentFlight);
            Booking.cancelBooking(cancel_Passenger, cancel_Date);
            status += "Passenger " + cancel_Passenger + " Cancelled.";
            
            String book_passenger = Waitlist.waitlistPassengerDate(currentFlight, cancel_Date);
            if (book_passenger != null)
            {
                java.sql.Timestamp newTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
                Booking.bookFlight(cancel_Date,currentFlight, book_passenger, newTimestamp);
                status +=  book_passenger + " was added";
                Waitlist.cancelWaitlist(book_passenger, cancel_Date);
            }
            
            
            CancelBookingJlabel.setText(status);
       
        }
        else
        {
            System.out.println("Passenger was not found in bookings, Checking Waitlist");
            number = Waitlist.cancelWaitlist(cancel_Passenger, cancel_Date);
            
            if (number == 0)
            {
                CancelBookingJlabel.setText("Passenger is not booked instead waitlisted for this date");
            }
            else
            {
                CancelBookingJlabel.setText("Passenger removed from waitlist for this date");
            }
        }
    }//GEN-LAST:event_CancelPassengerButtonActionPerformed

