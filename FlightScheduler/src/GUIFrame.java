    private void AddBookingButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBookingButtonActionPerformed
        // TODO add your handling code here:
        String flightType = (String) BookFlight.getSelectedItem();
        String passengerName =  (String) BookPassenger.getSelectedItem();
        Date flightDate=null;
        SimpleDateFormat sdf1 = new SimpleDateFormat("yyyy-MM-dd");
        try {
            java.util.Date date = sdf1.parse((String) BookDate.getSelectedItem());
            flightDate = new java.sql.Date(date.getTime());
        } catch (ParseException ex) {
            Logger.getLogger(GUIFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
         
        
        int total_seats = Flight.getSeats(flightType);
        int booked_seats = Booking.getBookCount(flightType, flightDate);
        
        java.sql.Timestamp passengerPosition = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        if (total_seats > booked_seats)
        {
            Booking.addBooking(passengerName, flightDate, flightType);
            JOptionPane.showMessageDialog(this, "Booking succeed!","Success", JOptionPane.PLAIN_MESSAGE); 
            
        }
        else
        {
            Waitlist.addWaitlist(passengerName, flightDate, flightType, passengerPosition);
            JOptionPane.showMessageDialog(this, "Passenger added to waitlist!","Error", JOptionPane.PLAIN_MESSAGE); 
        }
    }//GEN-LAST:event_AddBookingButtonActionPerformed

